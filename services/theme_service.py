def _normalize_kpi_items(items: Any) -> list[tuple[str, str]]:
    if items is None:
        return []
    normalized: list[tuple[str, str]] = []
    if isinstance(items, dict):
        for k, v in items.items():
            normalized.append((str(k), str(v)))
        return normalized
    try:
        for item in items:
            if isinstance(item, dict):
                label = item.get("label") or item.get("title") or item.get("name") or ""
                value = item.get("value") or item.get("number") or item.get("count") or ""
                normalized.append((str(label), str(value)))
            elif isinstance(item, (list, tuple)) and len(item) >= 2:
                normalized.append((str(item[0]), str(item[1])))
            else:
                normalized.append(("", str(item)))
    except Exception:
        pass
    return normalized


def render_kpi_cards(items: Any = None, *args: Any, **kwargs: Any) -> None:
    """Render KPI cards. Compatible with old tuple/list/dict calls."""
    cards = _normalize_kpi_items(items)
    if not cards and args:
        cards = _normalize_kpi_items(args[0])
    if not cards:
        cards = [("核心模組 / Modules", "12"), ("資料庫 / Database", "SQLite"), ("雲端保存 / Cloud", "GitHub"), ("狀態 / Status", "Online")]
    html = ['<div class="spt-card-grid">']
    for label, value in cards:
        html.append(f'<div class="spt-card"><div class="spt-card-label">{label}</div><div class="spt-card-value">{value}</div></div>')
    html.append("</div>")
    st.markdown("".join(html), unsafe_allow_html=True)


def _normalize_modules(modules: Any = None) -> list[tuple[str, str, str]]:
    default = [
        ("01", "工時紀錄", "快速開始、同步作業、暫停、下班、完工與工時計算"),
        ("02", "歷史紀錄", "完整工時明細查詢、編輯、儲存與 Excel 匯出"),
        ("03", "製令管理", "Excel 匯入、貼上資料、手動新增與製令主檔維護"),
        ("04", "人員名單", "人員主檔、在廠狀態、今日出勤與名單維護"),
        ("05", "製令工時分析", "製令累積工時、工段分析與明細查詢"),
        ("06", "LOG查詢", "系統操作、異常與資料異動紀錄查詢"),
        ("07", "今日未紀錄名單", "出勤但未登錄工時的人員即時提示"),
        ("08", "人員每日工時", "每日累積工時、合理區間與異常提醒"),
        ("09", "資料永久保存與備份", "GitHub 雲端永久保存與還原"),
        ("10", "權限管理", "帳號、角色與模組權限設定"),
        ("11", "登入紀錄", "登入、登出與安全事件查詢"),
        ("12", "模組永久紀錄中心", "各模組獨立永久紀錄檔與設定檔"),
    ]
    if modules is None:
        return default
    out: list[tuple[str, str, str]] = []
    try:
        for i, m in enumerate(modules, start=1):
            if isinstance(m, dict):
                out.append((str(m.get("no") or f"{i:02d}"), str(m.get("name") or m.get("title") or ""), str(m.get("desc") or m.get("description") or "")))
            elif isinstance(m, (list, tuple)):
                no = str(m[0]) if len(m) > 0 else f"{i:02d}"
                name = str(m[1]) if len(m) > 1 else ""
                desc = str(m[2]) if len(m) > 2 else ""
                out.append((no, name, desc))
    except Exception:
        return default
    return out or default


def render_module_cards(modules: Any = None, *args: Any, **kwargs: Any) -> None:
    cards = _normalize_modules(modules)
    html = ['<div class="spt-card-grid">']
    for no, name, desc in cards:
        html.append(
            f'<div class="spt-module-card"><div class="spt-module-no">{no}</div><div class="spt-module-name">{name}</div><div class="spt-module-desc">{desc}</div></div>'
        )
    html.append("</div>")
    st.markdown("".join(html), unsafe_allow_html=True)
