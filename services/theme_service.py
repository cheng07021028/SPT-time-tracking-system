def _find_logo() -> Path | None:
    for p in LOGO_PATHS:
        if p.exists() and p.is_file():
            return p
    return None


def _img_to_base64(path: Path) -> str:
    return base64.b64encode(path.read_bytes()).decode("utf-8")


def _logo_html() -> str:
    logo = _find_logo()
    if logo:
        try:
            return f'<img class="spt-logo-img" src="data:image/png;base64,{_img_to_base64(logo)}" />'
        except Exception:
            pass
    return '<div class="spt-logo-fallback">SPT</div>'


